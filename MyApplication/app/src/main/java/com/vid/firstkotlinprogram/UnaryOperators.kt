package com.vid.firstkotlinprogram

fun main(args: Array<String>) {

    var number: Double = 7.6
    var isChecked: Boolean = true

    println("+number = " + +number)
    println("-number = " + -number)
    println("++number = " + ++number)
    println("--number = " + --number)
    println("!isChecked = " + !isChecked)

    println("----------------------------")

    var result: Double = 4.7

    println("result++ = " + result++)
    println("result = " + result)
    println("++result = " + ++result)
    println("result = " + result)

}